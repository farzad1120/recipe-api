"""
Calculator functions
"""


def add(x, y):
    """
    Adds two numbers
    :param x: first number
    :param y: second number
    :return: result of addition
    """
    return x + y


def subtract(x, y):
    """
    Subtracts two numbers
    :param x: first number
    :param y: second number
    :return: result of subtraction
    """
    return x - y
